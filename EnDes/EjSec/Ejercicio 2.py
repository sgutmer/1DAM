# Calcular el perímetro y área de un rectángulo dada su base y su altura.
base = float(input("Indica cuanto mide la base: "));
altura = float(input("Indica cuanto mide la altura: "));
perimetro = 2 * (base + altura);
print("El peímetro del rectángulo mide: " + perimetro);
area = base * altura
print("El área del rectángulo mide: " + area);